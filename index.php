      <p class="f5 f4-ns lh-copy measure center fade">After a fun-filled summer, Meg and El headed back to
      Calgary. The next two years became very busy with school and  work&mdash;they soon realized that they needed
      to make the most of their limited time together, so they decided to take weekend adventures whenever
      they could. These mini experiences would take them to Banff, Emerald Lake Lodge, and Santa Monica. One of
      their most memorable mini Alberta adventures was their trip up to Red Deer to get their puppers, Harper! It was a long drive home with a very nervous pup, but Meg and El were so excited their pack had just grown by one.</p>

