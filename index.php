      couple decided that they would move to wherever the best opportunity was for him. After a few months of
      uncertainty they decided to move to Vancouver. The three of them packed up their home in Calgary and started out on their next adventure!</p>

      <img class="db w-100 pt4 pb4 fade" src="./img/fin.jpg" alt="Elliot and Meggi">

