      <h1 class="f2">About Vancouver</h1>
      <h2 class="f3" id="where-to-stay">Where to stay</h2>
      <p class="f5 f4-ns lh-copy measure">Vancouver is busy and expensive, especially in the summer. Make sure you give yourself plenty of time to find a place to stay.</p>
      <p class="f5 f4-ns lh-copy measure">If you're travelling with your family or are comfortable splitting a home with a group of friends, <a href="https://www.airbnb.ca/s/Vancouver--BC--Canada/homes?place_id=ChIJs0-pQ_FzhlQRi_OBm-qWkbs&query=Vancouver%2C%20BC%2C%20Canada&refinement_paths%5B%5D=%2Fhomes&allow_override%5B%5D=&s_tag=oGRjBFh1">Airbnb</a> may be a good option for you. Looking for less hassle and full service, the wedding is downtown so there are plenty of hotels to choose from. On a budget? Save some money and stay at the <a href="http://hihostels.ca/en/destinations/british-columbia/hi-vancouver-jericho-beach">Jericho Beach Hostel</a>. There is also nearby camping at <a href="http://www.capilanoriverrvpark.com/">Capilano River</a> or at <a href="https://bcrvpark.com/">Burnaby Moutain</a>.</p>

      <h2 class="f3 pt4" id="getting-around">Getting around</h2>
      <p class="f5 f4-ns lh-copy measure fade">There's no Uber here! That shouldn't be a problem though, plenty of Vancouver is very accessible by foot, bike or public transit. If you plan on using public transit more than once, it's worth picking up a <a href="https://www.compasscard.ca/">Compass Card.</a> Otherwise, booking a cab is usually fairly reliable using the Yellow Cab App on <a href="https://play.google.com/store/apps/details?id=com.limolabs.vancouveryc&hl=en">Android</a> or <a href="https://itunes.apple.com/ca/app/vancouver-yc/id1178805569?mt=8">iOS</a>.</p>

