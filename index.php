      <p class="f5 f4-ns lh-copy measure center fade">When they got home from this trip, Meg filled Elliot in on what he
      had said (for some reason he couldn’t remember) and told him that she would be staying in Calgary with him!
      During the next couple of years Elliot and Meggi spent their time exploring Calgary and the Rocky Mountains.</p>

      <img class="db w-100 pt4 pb4 fade" src="./img/megCanmore.jpg" alt="Meggi staring at the Rockies in
      Canmore">

      <p class="f5 f4-ns lh-copy measure center fade">In 2014, Elliot and Meggi decided to sublet their home in
      Calgary and drive across the country to live in Southern Ontario for a summer. They spent their time
      exploring Toronto’s food scene (which included trying to get through the <em>Top 20 Best Bowls of
        Ramen</em>). This adventure also gave Meg and Elliot an unforgettable Summer with the
      Marras! They spent most of their time playing pick-up basketball and hanging out in the backyard.</p>

      <blockquote class="ph0 f4 f1-ns measure-narrow center black-90 fade">
        <p class="fw9 lh-copy pl4-ns lh-title-ns">One of their most memorable mini Alberta adventures was their trip up to Red Deer to get their puppers, Harper!</p>
